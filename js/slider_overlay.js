$(document).ready(function() {
    $("#owl-demo").owlCarousel({
        navigation: true,
        dots: true,  // Affiche les indicateurs de navigation en bas
        slideSpeed: 300,
        paginationSpeed: 400,
        items: 1,  // Affiche 1 diapositive à la fois
        loop: true,  // Permet de faire une boucle infinie
        margin: 10,  // Marge entre les diapositives
        autoplay: true,  // Démarre automatiquement le défilement
        autoplayTimeout: 4000,  // Délai entre chaque diapositive en millisecondes
        autoplayHoverPause: true  // Met en pause le défilement automatique lorsqu'on survole le slider
    });
});

